            <h4>品牌核心-4H</h4>
            1、High taste高品味---拒绝指接
            真正的实木家具绝不能使用指接板。因为指接板是由木材加工产生的边角余料或废料通过指接工艺，再用大量胶水粘结而成。指接板首先很不美观，而且边角料做成的实木家具没有任何价值感，同时大大降低了实木家具的品味；
            2、High value高价值---拒绝辅材及任何形式的贴面
            真正好的实木家具是不能采用辅材和任何形式的贴面工艺的。市场上大多数实木家具都混合采用两种以上的实木材质。还有些实木家具采用木工板、集成材等很差的实木基材再在 表面通过贴面工艺贴上一层较好的薄薄的一层实木木皮或实木薄片，这样的家具的材质也都是实木成分的，但因为混合了辅材、木工板、集成版以及实木木皮或薄片等其它木种，使得家具以次充好，完全丧失了实木家具本身的价值，同时给产品质量也带来了无法避免的问题。因为不同的实木材质的含水率、密度、硬度、吸水膨胀和失水收缩性是完全不同的，因此不同的实木材质混合使用在同一件产品中时，必然会造成产品较为严重的开裂或变形，形成严重的不可避免和无法修复的品质问题。
            3、High safety高安全---拒绝甲醛超标 
            真正具有使用安全性的实木家具，首先要保证其甲醛释放低于欧盟顶级标准。由于市场上很多实木家具都采用指接板作为主要材料，而指接板会使用大量的胶水，因此国家质检总局对国内实木家具做了大范围检查，结果显示：约70%以上的实木家具的甲醛释放超过中国国家标准，会给长期接触此类家具的消费者的健康造成极大影响。 
            4、High quality高品质---拒绝实木板式化
            实木家具与板式家具一个很大的区别就在于制作工艺上。由于板式家具制造工艺相对简单，制作成本很低，因此现在很多实木家具都采用板式家具的制作工艺。但板式家具由于联接结构的工艺问题，通常结构不稳定，使用寿命较短，在国外多作为一次性家具使用。而真正好的实木家具通常可以传世使用，但要有传世的品质，就必须采用专业的实木家具制作工艺，主要有两种：一是榫卯结构工艺，二是360度无死角油漆工艺。
